//! Block storage Message commands
//!

use clap::{Parser, Subcommand};

use openstack_cli_core::{cli::CliArgs, error::OpenStackCliError};
use openstack_sdk::AsyncOpenStack;

pub mod delete_33;
pub mod list_33;
pub mod show_33;

/// Messages (messages)
///
/// Lists all, shows, and deletes messages. These are error messages generated by failed operations
/// as a way to find out what happened when an asynchronous operation failed.
#[derive(Parser)]
pub struct MessageCommand {
    /// subcommand
    #[command(subcommand)]
    command: MessageCommands,
}

/// Supported subcommands
#[allow(missing_docs)]
#[derive(Subcommand)]
pub enum MessageCommands {
    #[command(visible_alias = "delete")]
    Delete33(Box<delete_33::MessageCommand>),
    #[command(visible_alias = "list")]
    List33(Box<list_33::MessagesCommand>),
    #[command(visible_alias = "show")]
    Show33(Box<show_33::MessageCommand>),
}

impl MessageCommand {
    /// Perform command action
    pub async fn take_action<C: CliArgs>(
        &self,
        parsed_args: &C,
        session: &mut AsyncOpenStack,
    ) -> Result<(), OpenStackCliError> {
        match &self.command {
            MessageCommands::Delete33(cmd) => cmd.take_action(parsed_args, session).await,
            MessageCommands::List33(cmd) => cmd.take_action(parsed_args, session).await,
            MessageCommands::Show33(cmd) => cmd.take_action(parsed_args, session).await,
        }
    }
}
